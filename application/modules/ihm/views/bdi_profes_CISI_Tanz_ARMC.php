                    <div class="article-body">
                        <p>En <strong>mai 2025</strong>, dans le cadre du renforcement des capacités des acteurs du marché des capitaux au Burundi, un groupe de professionnels burundais, composé du staff <strong>de l'Autorité de Régulation du Marché des Capitaux du Burundi (ARMC)</strong> ainsi que de représentants des banques, des compagnies d'assurances et d'autres acteurs, a pris part à la passation de l'examen du <em>Chartered Institute for Securities and Investment (CISI)</em> du Royaume-Uni en Tanzanie.</p>
                        
                        <p>Cet examen constituait l'aboutissement d'un programme intensif de formation de six semaines, visant à outiller les participants sur les fondamentaux des marchés des capitaux, de la gouvernance financière et des bonnes pratiques internationales en matière d'investissement.</p>
                        
