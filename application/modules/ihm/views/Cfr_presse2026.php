                                <strong>En marge de ses deux années à l'œuvre, l'ARMC a présenté un bilan positif ce 4 mars 2026. Cette conférence de presse a également vu la participation des intermédiaires déjà agréés. Si la présentation des réalisations a été au cœur du programme, la conférence a également constitué un moment pédagogique pour expliquer les avantages du marché des capitaux burundais.</strong>
