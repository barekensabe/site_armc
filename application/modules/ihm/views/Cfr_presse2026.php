                            <p>L'Autorité de Régulation du Marché des Capitaux (ARMC) totalise déjà deux années d'activité au service du marché des capitaux burundais. Officiellement lancée le vendredi 9 février 2024, elle a procédé, ce 4 mars au Donatus Conference Center, à la présentation du bilan de ses réalisations, des défis rencontrés ainsi que de ses perspectives d'avenir. Cette conférence de presse a réuni les différents médias burundais ainsi que les intermédiaires déjà agréés sur le marché.</p>

                            <p>À cette occasion, le <strong>Directeur Général, Dr Mugenzi Arsène</strong>, a mis l'accent sur les avancées structurantes qui ont marqué l'action de l'ARMC au cours de ces deux années.</p>

                            <h3 id="bilan">Un bilan positif</h3>

                            <p>Bien que le marché des capitaux burundais existe depuis plusieurs années sur le plan légal, le lancement officiel de la <strong>Bourse du Burundi (BSE)</strong>, le <strong>12 décembre 2025</strong>, a consacré son opérationnalisation effective, ouvrant ainsi une nouvelle ère de financement pour l'économie nationale.</p>

