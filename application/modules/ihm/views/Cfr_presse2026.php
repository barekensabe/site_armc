                            <p>Parallèlement, l'ARMC a consolidé le cadre réglementaire à travers l'adoption de <strong>14 règlements et circulaires</strong>, tout en agréant <strong>neuf intermédiaires clés</strong>, renforçant progressivement l'écosystème du marché.</p>

                            <p>De surcroît, l'Autorité a intensifié ses missions de supervision et renforcé la conformité aux standards internationaux. En outre, un vaste <strong>programme d'éducation financière</strong>, incluant notamment une tournée nationale dans toutes les provinces du pays effectuée en février dernier, a été déployé afin de rapprocher le marché des capitaux des citoyens.</p>

                            <p>Enfin, l'organisation au Burundi d'une rencontre régionale de l'<strong>East Africa Securities Regulatory Authorities (EASRA)</strong>, ainsi que le renforcement des capacités internes par de nouveaux recrutements, témoignent de la volonté affirmée de positionner durablement le Burundi sur la carte financière régionale.</p>

                            <div class="quote">
                                "Nous avons posé des bases solides pour un marché des capitaux transparent, équitable et efficient au service du développement économique du Burundi."
                                <br>- Dr Mugenzi Arsène, Directeur Général de l'ARMC
                            </div>

                            <h3 id="attentes">Des attentes fortes autour du marché</h3>

                            <p>À travers les questions des journalistes, l'intérêt croissant pour le développement du marché des capitaux s'est clairement manifesté. Les échanges ont notamment porté sur la manière dont l'État, les collectivités locales et les citoyens peuvent tirer pleinement profit de l'opérationnalisation du marché.</p>

                            <p>Le Dr Mugenzi a rassuré l'opinion en mettant en avant l'agenda stratégique de l'ARMC et les produits adaptés aux différentes catégories d'acteurs. Dans cette dynamique, il a rappelé son engagement à contribuer à la <strong>restructuration des entreprises publiques</strong> afin de les conformer aux exigences du marché des capitaux. Une fois éligibles, ces entreprises pourront financer des projets structurants et rentables, contribuant ainsi davantage aux recettes de l'État plutôt que de dépendre des subventions publiques.</p>

                            <p>Ensuite, à travers les <strong>obligations municipales</strong>, le Directeur général a souligné l'opportunité offerte aux collectivités locales de mobiliser des ressources à long terme pour la réalisation de projets d'envergure.</p>

                            <h3 id="citoyens">Quid du profit pour le citoyen</h3>

                            <p>Quant aux citoyens, il a insisté sur le caractère inclusif du marché des capitaux, qui leur permet de valoriser leur épargne, quel qu'en soit le montant. Par ailleurs, les acteurs du secteur informel peuvent y participer en tant qu'investisseurs, même s'ils ne remplissent pas encore les conditions pour devenir émetteurs.</p>

                            <p>L'ARMC entend également mettre en place des mécanismes adaptés au financement des <strong>start-up à fort potentiel</strong>, malgré leur niveau de risque élevé.</p>

                            <div class="highlight">
                                <h4>📢 Engagement pour la transparence</h4>
                                <p>À travers cette rencontre avec la presse, l'ARMC a réaffirmé son engagement en faveur de la transparence et du partage d'informations utiles. Le Dr Mugenzi a, enfin, réitéré sa volonté de collaborer étroitement avec les médias afin de vulgariser le marché des capitaux burundais de manière inclusive et accessible à tous les niveaux.</p>
