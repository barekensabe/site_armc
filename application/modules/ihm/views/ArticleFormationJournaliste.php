                            <p>Au cours de cette formation, les participants ont échangé sur les principes éthiques et déontologiques que tout journaliste doit respecter dans la collecte, le traitement et la publication de l'information financière liée au marché des capitaux. Les discussions ont notamment porté sur la responsabilité sociale du journaliste, la fiabilité des sources, la lutte contre la désinformation, ainsi que la prévention des conflits d'intérêts.</p>
                            
