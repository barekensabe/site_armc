    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Fonction pour afficher une notification
            function showNotification(message) {
                const notification = document.createElement('div');
                notification.className = 'notification';
                notification.innerHTML = `
                    <p>${message}</p>
                `;
                document.body.appendChild(notification);
                
                setTimeout(() => {
                    notification.remove();
                }, 3000);
            }
            
            // Search functionality
            const searchModal = document.getElementById('searchModal');
            const openSearchBtn = document.getElementById('openSearch');
            const closeSearchBtn = document.getElementById('closeSearch');
            const searchInput = document.getElementById('searchInput');
            const searchResults = document.getElementById('searchResults');
            const performSearchBtn = document.getElementById('performSearch');

            const sampleArticles = [
                { title: "Atelier de formation des journalistes - Octobre 2025", category: "Événement", date: "Octobre 2025" },
                { title: "Instruction FIC 2024", category: "Régulation", date: "15/11/2024" },
                { title: "Guide des investisseurs 2024", category: "Documentation", date: "30/09/2024" },
                { title: "Rapport annuel 2023", category: "Publication", date: "15/10/2024" },
                { title: "Nouvelle structure des frais", category: "Régulation", date: "22/08/2024" },
                { title: "Protection des investisseurs", category: "Article", date: "10/07/2024" }
            ];

            // Ouvrir la modal de recherche
            openSearchBtn.addEventListener('click', () => {
                searchModal.classList.add('active');
                searchInput.focus();
            });
            
            // Fermer la modal de recherche
            closeSearchBtn.addEventListener('click', () => searchModal.classList.remove('active'));

            // Fermer la modal en cliquant en dehors
            window.addEventListener('click', (event) => {
                if (event.target === searchModal) {
                    searchModal.classList.remove('active');
                }
            });

            function performSearch() {
                const query = searchInput.value.trim().toLowerCase();
                if (query.length < 2) {
                    searchResults.innerHTML = '<p class="no-results">Veuillez saisir au moins 2 caractères.</p>';
                    return;
                }
                
                const filtered = sampleArticles.filter(article => 
                    article.title.toLowerCase().includes(query) || 
                    article.category.toLowerCase().includes(query)
                );
                
                displayResults(filtered);
            }

            function displayResults(results) {
                searchResults.innerHTML = '';
                if (results.length === 0) {
                    searchResults.innerHTML = '<p class="no-results">Aucun résultat trouvé. Essayez avec d\'autres mots-clés.</p>';
                    return;
                }
                
                results.forEach(article => {
                    const div = document.createElement('div');
                    div.className = 'search-result-item';
                    div.innerHTML = `
                        <h4>${article.title}</h4>
                        <p><small>${article.category} • ${article.date}</small></p>
                    `;
                    div.addEventListener('click', () => {
                        showNotification(`Ouverture de: ${article.title}`);
                        searchModal.classList.remove('active');
                        searchInput.value = '';
                        searchResults.innerHTML = '';
                    });
                    searchResults.appendChild(div);
                });
            }

            performSearchBtn.addEventListener('click', performSearch);
            searchInput.addEventListener('keyup', (e) => {
                if (e.key === 'Enter') performSearch();
            });

            // Effacer les résultats quand l'input est vide
            searchInput.addEventListener('input', (e) => {
                if (e.target.value === '') {
                    searchResults.innerHTML = '';
                }
            });
            
            // Fonction pour afficher la date actuelle
            function updateCurrentDate() {
                const currentDate = new Date();
                const options = { day: '2-digit', month: '2-digit', year: 'numeric' };
                const formattedDate = currentDate.toLocaleDateString('fr-FR', options);
                const sidebarDateElement = document.getElementById('sidebarDate');
                
                if (sidebarDateElement) {
                    sidebarDateElement.innerHTML = formattedDate;
                }
            }
            
            // Contrôle de la bannière défilante
            const marqueeContent = document.getElementById('marqueeContent');
            const marqueeControl = document.getElementById('marqueeControl');
            const controlIcon = marqueeControl.querySelector('i');
            let isPaused = false;
            
            marqueeControl.addEventListener('click', function() {
                if (isPaused) {
                    // Reprendre le défilement
                    marqueeContent.style.animationPlayState = 'running';
                    controlIcon.className = 'fas fa-pause';
                    marqueeControl.title = 'Pause';
                } else {
                    // Pause le défilement
                    marqueeContent.style.animationPlayState = 'paused';
                    controlIcon.className = 'fas fa-play';
                    marqueeControl.title = 'Play';
                }
                isPaused = !isPaused;
            });
            
            // Appeler la fonction pour afficher la date
            updateCurrentDate();
            
            // Smooth scroll pour les ancres
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    
                    const targetId = this.getAttribute('href');
                    if(targetId === '#') return;
                    
                    const targetElement = document.querySelector(targetId);
                    if(targetElement) {
                        window.scrollTo({
                            top: targetElement.offsetTop - 100,
                            behavior: 'smooth'
                        });
                    }
                });
            });
            
            // Logique pour masquer/afficher la sidebar au scroll
            const sidebar = document.querySelector('.quick-access-sidebar');
            const mainContent = document.querySelector('.main-content');
            let lastScrollTop = 0;
            let isSidebarVisible = true;
            
            // Initialiser la sidebar comme visible
            sidebar.classList.add('visible');
            sidebar.classList.remove('hidden');
            
            window.addEventListener('scroll', function() {
                const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
                
                // Masquer la sidebar lors du scroll down (sauf si on est tout en haut)
                if (scrollTop > lastScrollTop && scrollTop > 100) {
                    // Scroll down - masquer la sidebar
                    if (isSidebarVisible) {
                        sidebar.classList.remove('visible');
                        sidebar.classList.add('hidden');
                        mainContent.classList.add('sidebar-hidden');
                        isSidebarVisible = false;
                    }
                } 
                // Afficher la sidebar lors du scroll up
                else if (scrollTop < lastScrollTop) {
                    // Scroll up - afficher la sidebar
                    if (!isSidebarVisible) {
                        sidebar.classList.remove('hidden');
                        sidebar.classList.add('visible');
                        mainContent.classList.remove('sidebar-hidden');
                        isSidebarVisible = true;
                    }
                }
                
                // Toujours afficher la sidebar si on est près du haut de la page
                if (scrollTop <= 100) {
                    if (!isSidebarVisible) {
                        sidebar.classList.remove('hidden');
                        sidebar.classList.add('visible');
                        mainContent.classList.remove('sidebar-hidden');
                        isSidebarVisible = true;
                    }
                }
                
                lastScrollTop = scrollTop;
            });
            
            // Désactiver l'effet de scroll sur les mobiles (responsive)
            function checkWindowSize() {
                if (window.innerWidth <= 1200) {
                    // Sur mobile, désactiver l'effet de scroll
                    window.removeEventListener('scroll', arguments.callee);
                    sidebar.classList.remove('hidden');
                    sidebar.classList.add('visible');
                    mainContent.classList.remove('sidebar-hidden');
                    isSidebarVisible = true;
                }
            }
            
            // Vérifier la taille de la fenêtre au chargement
            checkWindowSize();
            
            // Vérifier la taille de la fenêtre lors du redimensionnement
            window.addEventListener('resize', checkWindowSize);
        });
    </script>