                            <p>Les journalistes présents ont salué cette initiative de l'ARMC, qu'ils considèrent comme une opportunité d'améliorer leurs connaissances sur les aspects techniques du marché des capitaux et les exigences réglementaires qui y sont associées. Ils ont également exprimé leur volonté de collaborer davantage avec l'ARMC pour une meilleure vulgarisation d'information auprès du public.</p>
                            
