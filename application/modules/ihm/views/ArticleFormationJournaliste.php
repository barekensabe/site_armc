                            <h3>Perspectives futures</h3>
                            
                            <p>Cet atelier marque le début d'une série d'initiatives visant à renforcer la collaboration entre l'ARMC et les médias. D'autres formations similaires sont prévues dans les prochains mois, avec pour objectif de créer un réseau de journalistes spécialisés dans les questions financières et de marchés de capitaux.</p>
