                            <p>En octobre 2025, l'Autorité de Régulation du Marché des Capitaux du Burundi <strong>(ARMC)</strong> a organisé, à Bujumbura à l'hôtel City Hill, un atelier de formation à l'intention des journalistes issus de divers médias. Cette rencontre avait pour objectif principal de présenter les dispositions du règlement relatif au Code de conduite dans le traitement de l'information, un document de référence visant à encadrer la diffusion des informations en lien avec le marché des capitaux au Burundi.</p>
                            
