                        <div class="article-body">
                            <h3>Définitions fondamentales</h3>
                            
                            <div class="definition">
                                <strong>Accord sur le marché des capitaux :</strong> tout accord dont l'objet constitue une activité du marché des capitaux définie au chapitre II de la présente loi.
                            </div>

                            <div class="definition">
                                <strong>Activités du marché des capitaux :</strong> les activités prévues au chapitre II de la présente loi, exercées directement par une entité permanente établie au Burundi, ou indirectement par l'intermédiaire d'un agent.
                            </div>

                            <div class="definition">
                                <strong>Agence de notation de crédit :</strong> une personne qui, à titre professionnel, fournit les services d'évaluation de la solvabilité des émetteurs de titres ou les titres eux-mêmes et attribue des cotes à ces émetteurs ou à ces titres.
                            </div>

                            <div class="definition">
                                <strong>Agent :</strong> une personne autorisée à agir au nom et pour le compte d'une autre personne lorsqu'elle interagit avec des tiers.
                            </div>

                            <div class="definition">
                                <strong>Associé :</strong> par rapport à une personne physique, le conjoint de cette personne, son enfant ou un enfant du conjoint, une personne morale dont cette personne est un administrateur, un employé ou partenaire. Par rapport à une personne morale, ses employés et/ou partenaires, sa filiale et tout employé ou partenaire de cette filiale.
                            </div>

                            <div class="definition">
                                <strong>Autorité :</strong> l'Autorité de Régulation du Marché des Capitaux.
                            </div>

                            <div class="definition">
                                <strong>Banque centrale :</strong> la Banque de la République du Burundi.
                            </div>

                            <div class="definition">
                                <strong>Bourse des valeurs :</strong> un établissement autorisé par l'Autorité où s'échangent les valeurs mobilières, c'est-à-dire des titres financiers négociables, interchangeables et fongibles : actions, obligations, titres de créances négociables, bons de souscription, certificats d'investissement, warrants, options et stocks options.
                            </div>

                            <div class="definition">
                                <strong>Chambre de compensation :</strong> une entité chargée de déterminer les obligations des membres négociant en terme de fonds et de titres et veille à ce que les transactions soient réglées par échange de ces obligations.
                            </div>

                            <div class="definition">
                                <strong>Conservateur des valeurs mobilières :</strong> une personne morale qui, à titre professionnel, est chargée de la garde de fonds, de titres, d'instruments financiers ou de documents de titres financiers.
                            </div>

                            <h3>Contrôle et gouvernance</h3>

                            <div class="definition">
                                <strong>Contrôleur :</strong>
                                <ol>
                                    <li>par rapport à une personne morale, une personne qui, seule ou avec un ou plusieurs associés, est en droit d'exercer ou de contrôler l'exercice de 15 % ou plus des droits de vote dans une assemblée générale de la personne morale ou d'une autre personne morale dont elle est une filiale.</li>
                                    <li>par rapport à une association non constituée en société :
                                        <ul>
                                            <li>une personne dont les orientations ou les instructions sont respectées par les responsables ou les membres de l'organe de direction de l'association, agissant habituellement seule ou avec un ou plusieurs associés, mis à part les conseils professionnels ;</li>
                                            <li>une personne qui, seule ou avec un ou plusieurs associés, est en droit d'exercer ou de contrôler l'exercice de 15 % ou plus des droits de vote aux assemblées générales de l'association.</li>
                                        </ul>
                                    </li>
                                </ol>
                            </div>

                            <div class="definition">
                                <strong>Dépositaire :</strong> une banque ou une société d'assurance à laquelle les placements collectifs ont été confiés pour leur garde et qui a, en outre, la responsabilité de superviser les opérations sur ces placements en relation à un organisme de placement collectif.
                            </div>

                            <div class="definition">
                                <strong>Dépositaire central de titres :</strong> un système établi par la Banque Centrale :
                                <ol>
                                    <li>pour le traitement centralisé des titres :
                                        <ul>
                                            <li>l'endroit où les titres sont déposés et détenus en garde ou enregistrés au nom de la société ou de sa société de prête-nom pour les déposants et transactions afférentes à ces titres effectuées au moyen d'entrées sur des comptes de titres sans remise de certificats ;</li>
                                            <li>qui permet ou facilite le règlement ou l'enregistrement des transactions de valeurs mobilières ou les opérations sur titres sans remise physique de certificats ;</li>
                                        </ul>
                                    </li>
                                    <li>qui fournit d'autres prestations et services connexes.</li>
                                </ol>
                            </div>

                            <div class="definition">
                                <strong>Entité :</strong> une personne morale exerçant des activités à but lucratif.
                            </div>

                            <h3>Délit d'initié et information privilégiée</h3>

                            <div class="definition">
                                <strong>Initié :</strong> une personne possédant des informations tout en ayant conscience qu'elles sont privilégiées et proviennent d'une source privilégiée. Une personne détenant des informations d'une source privilégiée lorsqu'elle les a directement ou indirectement par le fait qu'elle est directrice, employée, courtier ou actionnaire d'un émetteur de titres ou qu'elle a accès à ces informations en raison de son emploi, de sa fonction ou de sa profession.
                            </div>

                            <h3>Organismes de placement collectif</h3>

                            <div class="definition">
                                <strong>Opérateur d'organisme :</strong> en relation avec un organisme contractuel ou de partenariat, le gestionnaire d'un organisme ou, en relation avec les sociétés d'investissement, l'administrateur agréé d'une société auquel l'investissement des fonds de la société a été confié, dans le but de répartir les risques d'investissement.
                            </div>

                            <div class="definition">
                                <strong>Organisme de placement collectif :</strong>
                                <ol>
                                    <li>toute convention relative aux biens de toute nature y compris l'argent, dont le but ou l'effet est de permettre aux parties à cette convention de participer ou de recevoir des bénéfices ou des revenus provenant de l'acquisition, de la détention, de la gestion ou de la cession de ces biens ou des sommes reçues de ces bénéfices ou revenus ;</li>
                                    <li>les conventions faites de sorte que les participants, les investisseurs ou les souscripteurs, avec ou sans droit d'être consultés ou de donner des instructions, ne contrôlent pas au jour le jour la gestion des biens, des actifs ou des titres en question. Ces conventions doivent également avoir au moins une des caractéristiques suivantes :
                                        <ul>
                                            <li>les contributions des participants, des investisseurs ou des souscripteurs et les bénéfices ou les revenus sur lesquels les paiements doivent être effectués sont regroupés ;</li>
                                            <li>les biens en question sont gérés dans leur ensemble par l'opérateur de l'organisme ou en son nom.</li>
                                        </ul>
                                    </li>
                                </ol>
                            </div>

                            <div class="definition">
                                <strong>Panel :</strong> le comité indépendant de recours contre les décisions prises par l'Autorité et contre toutes les autres recommandations relatives au marché des capitaux.
                            </div>

                            <div class="definition">
                                <strong>Parts :</strong> par rapport à un organisme de placement collectif, les droits ou les intérêts, sous quelque forme qu'ils soient, des participants dans cet organisme.
                            </div>

                            <h3>Personnes et entités</h3>

                            <div class="definition">
                                <strong>Personne :</strong> toute personne physique ou morale.
                            </div>

                            <div class="definition">
                                <strong>Personne agréée :</strong> une personne ayant obtenu un agrément pour exercer une activité dans le cadre du marché des capitaux.
                            </div>

                            <div class="definition">
                                <strong>Personne autorisée :</strong> une personne de droit étranger reconnue par l'Autorité, en vertu de la présente loi, pour exercer une activité de marché des capitaux au Burundi.
                            </div>

                            <div class="definition">
                                <strong>Personne exemptée :</strong> une personne exonérée de l'obligation d'obtenir un agrément ou une autorisation de la part de l'Autorité du marché des capitaux.
                            </div>

                            <div class="definition">
                                <strong>Prête-nom :</strong> la personne dont le nom apparait sur un acte ou sur des documents, en lieu et place du réel contractant ou du réel instigateur et qui est juridiquement responsable et assume personnellement les charges du contrat.
                            </div>

                            <div class="definition">
                                <strong>Société apparentée :</strong> une personne morale qui est, par rapport à une autre personne morale faisant l'objet d'une enquête ou qui, à un moment donné, était :
                                <ol>
                                    <li>la société mère ou la filiale de la personne faisant l'objet d'une enquête administrative ou judiciaire ;</li>
                                    <li>la filiale d'une société mère de cette personne ;</li>
                                    <li>la société mère ou la filiale de cette personne dont les affaires, selon l'avis de l'Autorité, doivent être examinées, dans le but d'effectuer des enquêtes sur les affaires de cette personne.</li>
                                </ol>
                            </div>

                            <h3>Valeurs mobilières</h3>

                            <div class="definition">
                                <strong>Valeurs mobilières :</strong>
                                <ol>
                                    <li>les parts ou actions dans le capital social d'une société ;</li>
                                    <li>tout titre de créance créant ou reconnaissant l'endettement émis ou dont l'émission est projetée ;</li>
                                    <li>tout emprunt obligataire, toute obligation et tous les autres instruments créant ou reconnaissant l'endettement par ou pour le compte du Gouvernement, des collectivités locales, de la Banque Centrale ou des établissements publics ;</li>
                                    <li>les droits, les options ou les intérêts décrits comme les parts ou autrement dans ou relativement à ces actions, les titres de créance, les titres d'État ainsi que des titres publics ;</li>
                                    <li>tout droit, qu'il soit conféré par mandat ou autrement, de souscription d'actions ou titres de créances ;</li>
                                    <li>toute option d'acquisition ou d'aliénation de tout autre titre ;</li>
                                    <li>les options d'acquisition de titres ou autres actifs ou biens ;</li>
                                    <li>les parts dans un organisme de placement collectif, notamment dans une société d'investissement ou autres entités semblables établies ou non au Burundi ;</li>
                                    <li>les intérêts, les droits ou les biens sous la forme d'un instrument financier ou autrement, généralement connus sous le nom de valeurs mobilières ;</li>
                                    <li>les droits en vertu de tout certificat de dépôt à l'égard des actions, des titres de créance et des mandats ;</li>
                                    <li>les titres adossés à des actifs ;</li>
                                    <li>tout autre instrument prescrit par l'Autorité comme étant des valeurs mobilières aux fins de la présente loi.</li>
                                </ol>
                            </div>

                            <div class="highlight">
                                <p><strong>Ne sont pas inclus :</strong></p>
                                <ul>
                                    <li>les valeurs mobilières d'une société non cotée, autres que les titres adossés à des actifs ;</li>
                                    <li>les lettres de change ;</li>
                                    <li>les billets à ordre, autres que les titres adossés à des actifs ;</li>
                                    <li>les certificats de dépôt émis par une banque.</li>
                                </ul>
                            </div>
